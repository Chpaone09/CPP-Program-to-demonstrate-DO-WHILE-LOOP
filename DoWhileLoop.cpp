#include <iostream>
using namespace std;

int main(){
    int i=1;
    cout<<"\n\n\t Demonstration of DO-WHILE LOOP in C++ >> ";
    do{
        cout<<" "<<i++;
    }while(i <= 5);
    return 0;
}

